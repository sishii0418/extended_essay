function [lhs, rhs] = static_resid(y, x, params)
T = NaN(0, 1);
lhs = NaN(11, 1);
rhs = NaN(11, 1);
lhs(1) = y(3);
rhs(1) = y(4)-params(1)*y(10);
lhs(2) = y(8);
rhs(2) = params(1)*y(10)+y(6);
lhs(3) = y(9);
rhs(3) = y(8)+y(2);
lhs(4) = y(1);
rhs(4) = 0;
lhs(5) = y(3);
rhs(5) = y(3)-params(5)*(y(5)/12-y(1)/12);
lhs(6) = y(5);
rhs(6) = y(5)*params(7)+(1-params(7))*(y(1)*params(8)+y(3)*12*params(9));
lhs(7) = y(10);
rhs(7) = y(10)*params(10)+x(1);
lhs(8) = y(11);
rhs(8) = y(9);
lhs(9) = y(2);
rhs(9) = y(9)*(1-params(3))+params(3)*(y(2)+y(11)-y(9));
lhs(10) = y(7);
rhs(10) = y(3)*1/params(5)+y(4)*1/params(6);
lhs(11) = y(6);
rhs(11) = y(6)*params(4)+y(7)*(1-params(4));
end
