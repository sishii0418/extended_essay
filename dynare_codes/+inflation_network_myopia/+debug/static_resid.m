lhs = NaN(13, 1);
rhs = NaN(13, 1);
lhs(1) = y(4);
rhs(1) = y(5)-params(1)*y(10);
lhs(2) = y(8);
rhs(2) = params(5)*(y(11)-y(2))+(1-params(5))*y(7);
lhs(3) = y(9);
rhs(3) = y(8)+y(2);
lhs(4) = y(7);
rhs(4) = y(4)*1/params(7)+y(5)*1/params(8);
lhs(5) = y(1);
rhs(5) = 0;
lhs(6) = y(13);
rhs(6) = 0;
