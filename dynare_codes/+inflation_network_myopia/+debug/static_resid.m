lhs(8) = y(3);
rhs(8) = y(3)*params(3)*params(2)*params(6)+y(9)*(1-params(3)*params(2)*params(6));
lhs(9) = y(11);
rhs(9) = y(11)*params(4)+(1-params(4))*y(12);
lhs(10) = y(12);
rhs(10) = y(12)*params(2)*params(4)+(1-params(2)*params(4))*(params(1)*y(10)+y(2)+y(7));
lhs(11) = y(4);
rhs(11) = y(4)-params(7)*(y(6)/12-y(1)/12);
lhs(12) = y(6);
rhs(12) = y(6)*params(9)+(1-params(9))*(y(1)*params(10)+y(4)*12*params(11));
lhs(13) = y(10);
rhs(13) = y(10)*params(12)+x(1);
end
