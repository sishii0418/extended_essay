function [lhs, rhs] = static_resid(y, x, params)
T = NaN(0, 1);
lhs = NaN(12, 1);
rhs = NaN(12, 1);
lhs(1) = y(4);
rhs(1) = y(5)-params(1)*y(11);
lhs(2) = y(9);
rhs(2) = params(1)*y(11)+y(7);
lhs(3) = y(10);
rhs(3) = y(9)+y(2);
lhs(4) = y(1);
rhs(4) = 0;
lhs(5) = y(2);
rhs(5) = y(2)*params(3)+(1-params(3))*y(3);
lhs(6) = y(3);
rhs(6) = y(3)*params(3)*params(2)+y(10)*(1-params(3)*params(2));
lhs(7) = y(4);
rhs(7) = y(4)-params(5)*(y(6)/12-y(1)/12);
lhs(8) = y(6);
rhs(8) = y(6)*params(7)+(1-params(7))*(y(1)*params(8)+y(4)*12*params(9));
lhs(9) = y(11);
rhs(9) = y(11)*params(10)+x(1);
lhs(10) = y(8);
rhs(10) = y(4)*1/params(5)+y(5)*1/params(6);
lhs(11) = y(7);
rhs(11) = y(7)*params(4)+y(8)*(1-params(4));
lhs(12) = y(12);
rhs(12) = y(7)+y(2);
end
